package ncpl.bms.reports.model.dto;

public class TenantEnergyMeterDTO {
    private int id;
    private String name;
    private Integer tenantId; // Use Integer for nullable values

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getTenantId() {
        return tenantId;
    }

    public void setTenantId(Integer tenantId) {
        this.tenantId = tenantId;
    }
}
